package com.example.ddubuk;

public class festival {
    private String festivalname;
    private String festivalDate;
    private String festivalPhone;

    public String getFestivalname() {
        return festivalname;
    }

    public void setFestivalname(String festivalname) {
        this.festivalname = festivalname;
    }

    public String getFestivalDate() {
        return festivalDate;
    }

    public void setFestivalDate(String festivalDate) {
        this.festivalDate = festivalDate;
    }

    public String getFestivalPhone() {
        return festivalPhone;
    }

    public void setFestivalPhone(String festivalPhone) {
        this.festivalPhone = festivalPhone;
    }
}
